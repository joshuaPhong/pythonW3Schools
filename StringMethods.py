#  There are many built in python string methods
#  Here are some common examples
string_methods_string = "Hello world!, This string will be used in the " \
                        "examples of built in string methods"
#  upper() will convert a string to uppercase
string_upper = string_methods_string.upper()
#  lower() willconvert the string to lower case
string_lower = string_methods_string.lower()
#  strip() will remove leading and trailing whitespace
string_strip = string_methods_string.strip()
#  replace() will replace one string with another
string_replace = string_methods_string.replace("world", "Joshua")
#  split() will separate the string into substrings if it finds the seperator
#  split() is whitespace by default. the example below is an illustration.
#  The seperator in the "" could be any char
string_split = string_methods_string.split(" ")
